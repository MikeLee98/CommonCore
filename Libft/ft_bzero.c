#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
	// printf("%s", str);
}

